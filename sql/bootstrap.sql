GROUP BY r.run_id, r.status, r.started_at, r.finished_at;

-- Pick the coordinator-pushed encoding mode for a column. This is intentionally
-- conservative for types whose binary send format is less suitable as a
-- cross-version contract; the coordinator emits the chosen mode positionally in
-- row_digest(enc[], VARIADIC "any").
CREATE FUNCTION pgl_validate.column_encoding_mode(type_oid oid)
RETURNS int
LANGUAGE sql
STABLE
AS $$
    SELECT CASE
        WHEN type_oid IN ('json'::regtype::oid, 'numeric'::regtype::oid) THEN 2
        WHEN t.typsend = 0 THEN 2
        ELSE 1
    END
    FROM pg_type t
    WHERE t.oid = type_oid
$$;

-- Resolve the comparison key used for row-level localization. Prefer the
-- relation's replica identity index, then its primary key, then a simple
-- non-partial unique key whose NULL semantics make duplicate keys impossible.
CREATE FUNCTION pgl_validate.comparison_key_cols(relation regclass)
RETURNS text[]
LANGUAGE sql
STABLE
AS $$
    WITH candidates AS (
        SELECT i.*,
               CASE
                   WHEN i.indisreplident THEN 1
                   WHEN i.indisprimary THEN 2
                   ELSE 3
               END AS priority
        FROM pg_index i
        WHERE i.indrelid = $1
          AND i.indisvalid
          AND i.indisready
          AND i.indpred IS NULL
          AND i.indexprs IS NULL
          AND (
              i.indisreplident
              OR i.indisprimary
              OR (
                  i.indisunique
                  AND i.indimmediate
                  AND (
                      i.indnullsnotdistinct
                      OR NOT EXISTS (
                          SELECT 1
                          FROM unnest(i.indkey) WITH ORDINALITY AS key(attnum, ordinality)
                          JOIN pg_attribute a
                            ON a.attrelid = i.indrelid
                           AND a.attnum = key.attnum
                          WHERE key.ordinality <= i.indnkeyatts
                            AND NOT a.attnotnull
                      )
                  )
              )
          )
          AND NOT EXISTS (
              SELECT 1
              FROM unnest(i.indkey) WITH ORDINALITY AS key(attnum, ordinality)
              LEFT JOIN pg_attribute a
                ON a.attrelid = i.indrelid
               AND a.attnum = key.attnum
              WHERE key.ordinality <= i.indnkeyatts
                AND (key.attnum <= 0 OR a.attnum IS NULL OR a.attisdropped)
          )
    ),
    chosen AS (
        SELECT *
        FROM candidates
        ORDER BY priority, indexrelid
        LIMIT 1
    )
    SELECT array_agg(a.attname ORDER BY key.ordinality)
    FROM chosen i
    CROSS JOIN LATERAL unnest(i.indkey) WITH ORDINALITY AS key(attnum, ordinality)
    JOIN pg_attribute a
      ON a.attrelid = i.indrelid
     AND a.attnum = key.attnum
    WHERE key.ordinality <= i.indnkeyatts
$$;

-- Resolve the pglogical replication contract for one relation. The effective
-- column list is taken from pglogical.show_repset_table_info(), because that is
-- pglogical's own resolved bitmap after combining all covering repsets.
CREATE FUNCTION pgl_validate.pglogical_table_contract(
    relation regclass,
    input_repsets text[] DEFAULT NULL,
    subscription_name name DEFAULT NULL
)
RETURNS TABLE (
    schema_name text,
    table_name text,
    key_cols text[],
    att_list text[],
    repsets text[],
    repl_insert boolean,
    repl_update boolean,
    repl_delete boolean,
    repl_truncate boolean,
    has_row_filter boolean,
    row_filter_sql text,
    row_filter_exact boolean,
    sync_status "char",
    validated_property text,
    exact_comparable boolean,
    reason text
)
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
    info record;
    action_rec record;
    filter_rec record;
    status_text text;
BEGIN
    IF to_regprocedure('pglogical.show_repset_table_info(regclass,text[])') IS NULL THEN
        RAISE EXCEPTION 'pglogical extension is not installed in this database'
            USING ERRCODE = '0A000';
    END IF;

    SELECT n.nspname, c.relname
    INTO schema_name, table_name
    FROM pg_class c
    JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE c.oid = relation;

    key_cols := pgl_validate.comparison_key_cols(relation);

    IF input_repsets IS NULL OR cardinality(input_repsets) = 0 THEN
        SELECT array_agg(DISTINCT rs.set_name::text ORDER BY rs.set_name::text)
        INTO repsets
        FROM pglogical.replication_set_table rst
        JOIN pglogical.replication_set rs ON rs.set_id = rst.set_id
        WHERE rst.set_reloid = relation;
    ELSE
        SELECT array_agg(DISTINCT requested.repset ORDER BY requested.repset)
        INTO repsets
        FROM unnest(input_repsets) AS requested(repset);
    END IF;

    IF repsets IS NULL OR cardinality(repsets) = 0 THEN
        att_list := NULL;
        repl_insert := false;
        repl_update := false;
        repl_delete := false;
        repl_truncate := false;
        has_row_filter := false;
        row_filter_sql := NULL;
        row_filter_exact := true;
        sync_status := NULL;
        validated_property := 'skipped';
        exact_comparable := false;
        reason := 'table is not a member of any selected pglogical replication set';
        RETURN NEXT;
        RETURN;
    END IF;

    SELECT *
    INTO info
    FROM pglogical.show_repset_table_info(relation, repsets);

    att_list := info.att_list;
    IF att_list IS NULL OR cardinality(att_list) = 0 THEN
        SELECT array_agg(a.attname ORDER BY a.attname)
        INTO att_list
        FROM pg_attribute a
        WHERE a.attrelid = relation
          AND a.attnum > 0
          AND NOT a.attisdropped;
    END IF;

    SELECT bool_or(rs.replicate_insert) AS repl_insert,
           bool_or(rs.replicate_update) AS repl_update,
           bool_or(rs.replicate_delete) AS repl_delete,
           bool_or(rs.replicate_truncate) AS repl_truncate
    INTO action_rec
    FROM pglogical.replication_set rs
    WHERE rs.set_name::text = ANY (repsets);

    repl_insert := COALESCE(action_rec.repl_insert, false);
    repl_update := COALESCE(action_rec.repl_update, false);
    repl_delete := COALESCE(action_rec.repl_delete, false);
    repl_truncate := COALESCE(action_rec.repl_truncate, false);

    SELECT
        COALESCE(bool_or(rst.set_row_filter IS NULL), false) AS has_unfiltered_membership,
        count(*) FILTER (WHERE rst.set_row_filter IS NOT NULL) AS filtered_memberships,
        string_agg(
            format('(%s)', pg_get_expr(rst.set_row_filter, rst.set_reloid)),
            ' OR '
            ORDER BY rs.set_name::text
        ) FILTER (WHERE rst.set_row_filter IS NOT NULL) AS predicate,
        bool_and(
            pgl_validate.row_filter_tree_is_immutable(rst.set_row_filter::text)
            AND pg_get_expr(rst.set_row_filter, rst.set_reloid) !~*
                '(^|[^[:alnum:]_])(current_user|session_user|current_role|current_setting|set_config|current_schema|current_schemas)([^[:alnum:]_]|$)'
        ) FILTER (WHERE rst.set_row_filter IS NOT NULL) AS exact
    INTO filter_rec
    FROM pglogical.replication_set_table rst
    JOIN pglogical.replication_set rs ON rs.set_id = rst.set_id
    WHERE rst.set_reloid = relation
      AND rs.set_name::text = ANY (repsets);

    IF COALESCE(filter_rec.has_unfiltered_membership, false)
       OR COALESCE(filter_rec.filtered_memberships, 0) = 0 THEN
        has_row_filter := false;
        row_filter_sql := NULL;
        row_filter_exact := true;
    ELSE
        has_row_filter := true;
        row_filter_sql := filter_rec.predicate;
        row_filter_exact := COALESCE(filter_rec.exact, false);
    END IF;

    sync_status := 'r';
    IF subscription_name IS NOT NULL THEN
        SELECT s.status
        INTO status_text
        FROM pglogical.show_subscription_table(subscription_name, relation) AS s;

        IF status_text IS NOT NULL THEN
            sync_status := left(status_text, 1)::"char";
        END IF;
    ELSE
        SELECT lss.sync_status
        INTO sync_status
        FROM pglogical.local_sync_status lss
        WHERE lss.sync_nspname = schema_name::name
          AND lss.sync_relname = table_name::name
        ORDER BY lss.sync_statuslsn DESC
        LIMIT 1;

        sync_status := COALESCE(sync_status, 'r');
    END IF;

    IF sync_status <> 'r' THEN
        validated_property := 'skipped';
        exact_comparable := false;
        reason := format('pglogical sync status is %s, not ready', sync_status);
    ELSIF NOT repl_insert THEN
        validated_property := 'unsupported_mask';
        exact_comparable := false;
        reason := 'replicate_insert=false means the provider row set does not bound the subscriber';
    ELSIF has_row_filter AND repl_update AND row_filter_exact THEN
        validated_property := 'filtered_intersection';
        exact_comparable := true;
        reason := 'pglogical row filter is immutable/context-free; validating content intersection with advisory presence differences';
    ELSIF has_row_filter AND repl_update THEN
        validated_property := 'skipped';
        exact_comparable := false;
        reason := 'pglogical row filter is not immutable/context-free; exact validation would be session-sensitive';
    ELSIF has_row_filter THEN
        validated_property := 'filtered_advisory';
        exact_comparable := false;
        reason := 'pglogical filtered table without update replication is advisory only';
    ELSIF repl_update AND repl_delete AND repl_truncate THEN
        validated_property := 'full';
        exact_comparable := true;
        reason := 'full pglogical action mask with no row filter';
    ELSIF repl_update THEN
        validated_property := 'superset';
        exact_comparable := true;
        reason := 'delete or truncate is not replicated, so subscriber extras are legitimate';
    ELSE
        validated_property := 'keys_only';
        exact_comparable := true;
        reason := 'updates are not replicated, so content drift is contract-permitted';
    END IF;

    RETURN NEXT;
END
$$;

-- Resolve the native logical replication contract for one relation. The
-- effective table membership, column list, and row filter come from the core
-- pg_publication_tables view so FOR ALL TABLES and FOR TABLES IN SCHEMA are
-- expanded exactly as PostgreSQL does.
CREATE FUNCTION pgl_validate.native_table_contract(
    relation regclass,
    input_publications text[] DEFAULT NULL,
    subscription_name name DEFAULT NULL
)
RETURNS TABLE (
    schema_name text,
    table_name text,
    key_cols text[],
    att_list text[],
    repsets text[],
    repl_insert boolean,
    repl_update boolean,
    repl_delete boolean,
    repl_truncate boolean,
    has_row_filter boolean,
    row_filter_sql text,
    row_filter_exact boolean,
    sync_status "char",
    validated_property text,
    exact_comparable boolean,
    reason text
)
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
    action_rec record;
    filter_rec record;
    distinct_att_lists int;
BEGIN
    SELECT n.nspname, c.relname
    INTO schema_name, table_name
    FROM pg_class c
    JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE c.oid = relation;

    key_cols := pgl_validate.comparison_key_cols(relation);

    SELECT array_agg(DISTINCT pt.pubname::text ORDER BY pt.pubname::text),
           count(DISTINCT pt.attnames::text)
    INTO repsets, distinct_att_lists
    FROM pg_publication_tables pt
    WHERE pt.schemaname = schema_name
      AND pt.tablename = table_name
      AND (
          input_publications IS NULL
          OR cardinality(input_publications) = 0
          OR pt.pubname::text = ANY (input_publications)
      );

    IF repsets IS NULL OR cardinality(repsets) = 0 THEN
        att_list := NULL;
        repl_insert := false;
        repl_update := false;
        repl_delete := false;
        repl_truncate := false;
        has_row_filter := false;
        row_filter_sql := NULL;
        row_filter_exact := true;
        sync_status := NULL;
        validated_property := 'skipped';
        exact_comparable := false;
        reason := 'table is not a member of any selected native publication';
        RETURN NEXT;
        RETURN;
    END IF;

    IF distinct_att_lists > 1 THEN
        att_list := NULL;
        repl_insert := false;
        repl_update := false;
        repl_delete := false;
        repl_truncate := false;
        has_row_filter := false;
        row_filter_sql := NULL;
        row_filter_exact := false;
        sync_status := NULL;
        validated_property := 'skipped';
        exact_comparable := false;
        reason := 'native publications have incompatible column lists for this table';
        RETURN NEXT;
        RETURN;
    END IF;

    SELECT array_agg(DISTINCT att.attname::text ORDER BY att.attname::text)
    INTO att_list
    FROM pg_publication_tables pt
    CROSS JOIN LATERAL unnest(pt.attnames) AS att(attname)
    WHERE pt.schemaname = schema_name
      AND pt.tablename = table_name
      AND pt.pubname::text = ANY (repsets);

    SELECT bool_or(p.pubinsert) AS repl_insert,
           bool_or(p.pubupdate) AS repl_update,
           bool_or(p.pubdelete) AS repl_delete,
           bool_or(p.pubtruncate) AS repl_truncate
    INTO action_rec
    FROM pg_publication p
    WHERE p.pubname::text = ANY (repsets);

    repl_insert := COALESCE(action_rec.repl_insert, false);
    repl_update := COALESCE(action_rec.repl_update, false);
    repl_delete := COALESCE(action_rec.repl_delete, false);
    repl_truncate := COALESCE(action_rec.repl_truncate, false);

    SELECT
        COALESCE(bool_or(pt.rowfilter IS NULL), false) AS has_unfiltered_membership,
        count(*) FILTER (WHERE pt.rowfilter IS NOT NULL) AS filtered_memberships,
        string_agg(
            format('(%s)', pt.rowfilter),
            ' OR '
            ORDER BY pt.pubname::text
        ) FILTER (WHERE pt.rowfilter IS NOT NULL) AS predicate,
        bool_and(
            pt.rowfilter !~*
                '(^|[^[:alnum:]_])(current_user|session_user|current_role|current_setting|set_config|current_schema|current_schemas)([^[:alnum:]_]|$)'
        ) FILTER (WHERE pt.rowfilter IS NOT NULL) AS exact
    INTO filter_rec
    FROM pg_publication_tables pt
    WHERE pt.schemaname = schema_name
      AND pt.tablename = table_name
      AND pt.pubname::text = ANY (repsets);

    IF COALESCE(filter_rec.has_unfiltered_membership, false)
       OR COALESCE(filter_rec.filtered_memberships, 0) = 0 THEN
        has_row_filter := false;
        row_filter_sql := NULL;
        row_filter_exact := true;
    ELSE
        has_row_filter := true;
        row_filter_sql := filter_rec.predicate;
        row_filter_exact := COALESCE(filter_rec.exact, false);
    END IF;

    sync_status := 'r';
    IF subscription_name IS NOT NULL THEN
        SELECT sr.srsubstate
        INTO sync_status
        FROM pg_subscription s
        JOIN pg_subscription_rel sr ON sr.srsubid = s.oid
        JOIN pg_database d ON d.oid = s.subdbid
        WHERE s.subname = subscription_name
          AND d.datname = current_database()
          AND sr.srrelid = relation;
    END IF;

    IF sync_status IS NULL THEN
        validated_property := 'skipped';
        exact_comparable := false;
        reason := format('native subscription % has no sync state for this table', subscription_name);
    ELSIF sync_status <> 'r' THEN
        validated_property := 'skipped';
        exact_comparable := false;
        reason := format('native subscription sync status is %s, not ready', sync_status);
    ELSIF NOT repl_insert THEN
        validated_property := 'unsupported_mask';
        exact_comparable := false;
        reason := 'publish=insert is disabled, so the provider row set does not bound the subscriber';
    ELSIF has_row_filter AND NOT row_filter_exact THEN
        validated_property := 'skipped';
        exact_comparable := false;
        reason := 'native row filter is session-sensitive; exact validation would be context-dependent';
    ELSIF has_row_filter AND repl_update AND repl_delete AND repl_truncate THEN
        validated_property := 'full';
        exact_comparable := true;
        reason := 'native row filter with full action mask maintains S = P_F';
    ELSIF has_row_filter AND repl_update THEN
        validated_property := 'superset';
        exact_comparable := true;
        reason := 'native row filter is exact, but delete or truncate is not replicated, so subscriber extras are legitimate';
    ELSIF has_row_filter THEN
        validated_property := 'filtered_advisory';
        exact_comparable := false;
        reason := 'native filtered table without update replication is advisory only';
    ELSIF repl_update AND repl_delete AND repl_truncate THEN
        validated_property := 'full';
        exact_comparable := true;
        reason := 'full native publication action mask with no row filter';
    ELSIF repl_update THEN
        validated_property := 'superset';
        exact_comparable := true;
        reason := 'delete or truncate is not replicated, so subscriber extras are legitimate';
    ELSE
        validated_property := 'keys_only';
        exact_comparable := true;
        reason := 'updates are not replicated, so content drift is contract-permitted';
    END IF;

    RETURN NEXT;
END
$$;

-- Ensure the dedicated insert-only barrier replication set exists on the
-- current pglogical node and carries only the standalone barrier table.
CREATE FUNCTION pgl_validate.ensure_pglogical_barrier_repset()
RETURNS void
LANGUAGE plpgsql
VOLATILE
AS $$
DECLARE
    set_rec record;
    table_member boolean;
BEGIN
    IF to_regprocedure('pglogical.create_replication_set(name,boolean,boolean,boolean,boolean)') IS NULL THEN
        RAISE EXCEPTION 'pglogical extension is not installed in this database'
            USING ERRCODE = '0A000';
    END IF;

    SELECT rs.*
    INTO set_rec
    FROM pglogical.replication_set rs
    JOIN pglogical.local_node ln ON ln.node_id = rs.set_nodeid
    WHERE rs.set_name = 'pgl_validate_barrier'::name;

    IF NOT FOUND THEN
        PERFORM pglogical.create_replication_set(
            'pgl_validate_barrier'::name,
            true,
            false,
            false,
            false
        );
    ELSIF NOT (
        set_rec.replicate_insert
        AND NOT set_rec.replicate_update
        AND NOT set_rec.replicate_delete
        AND NOT set_rec.replicate_truncate
    ) THEN
        RAISE EXCEPTION
            'pglogical replication set pgl_validate_barrier exists but is not insert-only';
    END IF;

    IF EXISTS (
        SELECT 1
        FROM pglogical.replication_set rs
        JOIN pglogical.local_node ln ON ln.node_id = rs.set_nodeid
        JOIN pglogical.replication_set_table rst ON rst.set_id = rs.set_id
        WHERE rs.set_name = 'pgl_validate_barrier'::name
          AND rst.set_reloid <> 'pgl_validate.fence_barrier'::regclass
    ) THEN
        RAISE EXCEPTION
            'pglogical replication set pgl_validate_barrier must contain only pgl_validate.fence_barrier';
    END IF;

    SELECT EXISTS (
        SELECT 1
        FROM pglogical.replication_set rs
        JOIN pglogical.local_node ln ON ln.node_id = rs.set_nodeid
        JOIN pglogical.replication_set_table rst ON rst.set_id = rs.set_id
        WHERE rs.set_name = 'pgl_validate_barrier'::name
          AND rst.set_reloid = 'pgl_validate.fence_barrier'::regclass
    )
    INTO table_member;

    IF NOT table_member THEN
        PERFORM pglogical.replication_set_add_table(
            'pgl_validate_barrier'::name,
            'pgl_validate.fence_barrier'::regclass,
            false
        );
    END IF;
END
$$;

-- Fence one pglogical provider->target edge by injecting a real replicated
-- barrier, waiting for provider-side slot flush, then polling target-side
-- origin progress and token visibility until convergence or timeout.
CREATE FUNCTION pgl_validate.fence_pglogical_edge(
    p_run_id bigint,
    p_epoch_seq int,
    p_edge_id int,
    p_provider_node text,
    p_target_node text,
    p_provider_dsn text,
    p_target_dsn text,
    p_subscription_name text,
    p_slot_name text,
    p_origin_name text,
    p_repsets text[] DEFAULT ARRAY['pgl_validate_barrier']::text[],
    p_connect_timeout_seconds int DEFAULT 10,
    p_statement_timeout_ms int DEFAULT 600000,
    p_lock_timeout_ms int DEFAULT 30000,
    p_fence_timeout_ms int DEFAULT 300000,
    p_poll_interval_ms int DEFAULT 100
)
RETURNS pgl_validate.fence_attempt
LANGUAGE plpgsql
VOLATILE
AS $$
DECLARE
    injection record;
    confirmed_flush_lsn pg_lsn;
    observation record;
    deadline timestamptz;
    attempt_status text;
    result_row pgl_validate.fence_attempt;
BEGIN
    IF p_run_id IS NULL THEN
        RAISE EXCEPTION 'run_id is required';
    END IF;
    IF p_epoch_seq IS NULL THEN
        RAISE EXCEPTION 'epoch_seq is required';
    END IF;
    IF p_edge_id IS NULL THEN
        RAISE EXCEPTION 'edge_id is required';
    END IF;
    IF p_provider_node IS NULL OR p_target_node IS NULL THEN
        RAISE EXCEPTION 'provider_node and target_node are required';
    END IF;
    IF p_provider_dsn IS NULL OR p_target_dsn IS NULL THEN
        RAISE EXCEPTION 'provider_dsn and target_dsn are required';
    END IF;
    IF p_slot_name IS NULL THEN
        RAISE EXCEPTION 'slot_name is required';
    END IF;
    IF p_origin_name IS NULL THEN
        RAISE EXCEPTION 'origin_name is required';
    END IF;
    IF p_fence_timeout_ms <= 0 THEN
        RAISE EXCEPTION 'fence_timeout_ms must be greater than zero';
    END IF;
    IF p_poll_interval_ms <= 0 THEN
        RAISE EXCEPTION 'poll_interval_ms must be greater than zero';
    END IF;

    INSERT INTO pgl_validate.run_edge(
        run_id, edge_id, provider_node, target_node, backend,
        subscription, slot_name, origin_name, repsets
    )
    VALUES (
        p_run_id, p_edge_id, p_provider_node, p_target_node, 'pglogical',
        p_subscription_name, p_slot_name, p_origin_name, p_repsets
    )
    ON CONFLICT (run_id, edge_id) DO UPDATE
    SET provider_node = EXCLUDED.provider_node,
        target_node = EXCLUDED.target_node,
        backend = 'pglogical',
        subscription = EXCLUDED.subscription,
        slot_name = EXCLUDED.slot_name,
        origin_name = EXCLUDED.origin_name,
        repsets = EXCLUDED.repsets;

    SELECT *
    INTO injection
    FROM pgl_validate.remote_inject_barrier(
        p_provider_dsn,
        p_connect_timeout_seconds,
        p_statement_timeout_ms,
        p_lock_timeout_ms
    );

    PERFORM pgl_validate.record_barrier_fence(
        p_run_id,
        p_epoch_seq,
        p_edge_id,
        injection.token,
        p_provider_node,
        injection.barrier_end_lsn
    );

    SELECT pgl_validate.remote_wait_slot_confirm_lsn(
        p_provider_dsn,
        p_slot_name,
        injection.barrier_end_lsn,
        p_connect_timeout_seconds,
        p_statement_timeout_ms,
        p_lock_timeout_ms
    )
    INTO confirmed_flush_lsn;

    IF confirmed_flush_lsn < injection.barrier_end_lsn THEN
        RAISE EXCEPTION
            'slot % confirmed_flush_lsn % is behind barrier_end_lsn %',
            p_slot_name,
            confirmed_flush_lsn,
            injection.barrier_end_lsn;
    END IF;

    deadline := clock_timestamp() + make_interval(secs => p_fence_timeout_ms / 1000.0);
    LOOP
        SELECT *
        INTO observation
        FROM pgl_validate.remote_observe_barrier(
            p_target_dsn,
            p_origin_name,
            injection.token,
            injection.barrier_end_lsn,
            p_connect_timeout_seconds,
            p_statement_timeout_ms,
            p_lock_timeout_ms
        );

        EXIT WHEN observation.converged;

        IF clock_timestamp() >= deadline THEN
            attempt_status := 'timeout';
            EXIT;
        END IF;

        PERFORM pg_sleep(p_poll_interval_ms / 1000.0);
    END LOOP;

    SELECT *
    INTO result_row
    FROM pgl_validate.record_fence_attempt(
        p_run_id,
        p_epoch_seq,
        p_edge_id,
        injection.barrier_end_lsn,
        observation.origin_progress_lsn,
        observation.token_visible,
        confirmed_flush_lsn,
        attempt_status
    );

    RETURN result_row;
END
$$;

-- Record an explicit degraded pglogical fence for an edge that cannot carry the
-- barrier table. This is never exact: it waits only for provider-side slot
-- flush through a captured WAL LSN and persists status=degraded.
CREATE FUNCTION pgl_validate.fence_pglogical_degraded_edge(
    p_run_id bigint,
    p_epoch_seq int,
    p_edge_id int,
    p_provider_node text,
    p_target_node text,
    p_provider_dsn text,
    p_subscription_name text,
    p_slot_name text,
    p_origin_name text,
    p_repsets text[] DEFAULT NULL,
    p_connect_timeout_seconds int DEFAULT 10,
    p_statement_timeout_ms int DEFAULT 600000,
    p_lock_timeout_ms int DEFAULT 30000
)
RETURNS pgl_validate.fence_attempt
LANGUAGE plpgsql
VOLATILE
AS $$
DECLARE
    target_lsn pg_lsn;
    confirmed_flush_lsn pg_lsn;
    result_row pgl_validate.fence_attempt;
BEGIN
    IF p_run_id IS NULL THEN
        RAISE EXCEPTION 'run_id is required';
    END IF;
    IF p_epoch_seq IS NULL THEN
        RAISE EXCEPTION 'epoch_seq is required';
    END IF;
    IF p_edge_id IS NULL THEN
        RAISE EXCEPTION 'edge_id is required';
    END IF;
    IF p_provider_node IS NULL OR p_target_node IS NULL THEN
        RAISE EXCEPTION 'provider_node and target_node are required';
    END IF;
    IF p_provider_dsn IS NULL THEN
        RAISE EXCEPTION 'provider_dsn is required';
    END IF;
    IF p_slot_name IS NULL THEN
        RAISE EXCEPTION 'slot_name is required';
    END IF;
    IF p_origin_name IS NULL THEN
        RAISE EXCEPTION 'origin_name is required';
    END IF;

    INSERT INTO pgl_validate.run_edge(
        run_id, edge_id, provider_node, target_node, backend,
        subscription, slot_name, origin_name, repsets
    )
    VALUES (
        p_run_id, p_edge_id, p_provider_node, p_target_node, 'pglogical',
        p_subscription_name, p_slot_name, p_origin_name, p_repsets
    )
    ON CONFLICT (run_id, edge_id) DO UPDATE
    SET provider_node = EXCLUDED.provider_node,
        target_node = EXCLUDED.target_node,
        backend = 'pglogical',
        subscription = EXCLUDED.subscription,
        slot_name = EXCLUDED.slot_name,
        origin_name = EXCLUDED.origin_name,
        repsets = EXCLUDED.repsets;

    SELECT pgl_validate.remote_current_wal_lsn(
        p_provider_dsn,
        p_connect_timeout_seconds,
        p_statement_timeout_ms,
        p_lock_timeout_ms
    )
    INTO target_lsn;

    SELECT pgl_validate.remote_wait_slot_confirm_lsn(
        p_provider_dsn,
        p_slot_name,
        target_lsn,
        p_connect_timeout_seconds,
        p_statement_timeout_ms,
        p_lock_timeout_ms
    )
    INTO confirmed_flush_lsn;

    IF confirmed_flush_lsn < target_lsn THEN
        RAISE EXCEPTION
            'slot % confirmed_flush_lsn % is behind degraded target_lsn %',
            p_slot_name,
            confirmed_flush_lsn,
            target_lsn;
    END IF;

    INSERT INTO pgl_validate.fence_epoch(run_id, epoch_seq)
    VALUES (p_run_id, p_epoch_seq)
    ON CONFLICT DO NOTHING;

    INSERT INTO pgl_validate.fence_edge(
        run_id, epoch_seq, edge_id, fence_kind, barrier_token, barrier_end_lsn
    )
    VALUES (
        p_run_id,
        p_epoch_seq,
        p_edge_id,
        'degraded',
        NULL,
        target_lsn
    )
    ON CONFLICT (run_id, epoch_seq, edge_id) DO UPDATE
    SET fence_kind = 'degraded',
        barrier_token = NULL,
        barrier_end_lsn = EXCLUDED.barrier_end_lsn;

    SELECT *
    INTO result_row
    FROM pgl_validate.record_fence_attempt(
        p_run_id,
        p_epoch_seq,
        p_edge_id,
        target_lsn,
        NULL,
        false,
        confirmed_flush_lsn,
        'degraded'
    );

    RETURN result_row;
END
$$;

-- Fence one primary->physical-standby edge by capturing a primary WAL LSN and
-- polling the target until physical replay has reached that cut.
CREATE FUNCTION pgl_validate.fence_standby_edge(
    p_run_id bigint,
    p_epoch_seq int,
    p_edge_id int,
    p_provider_node text,
    p_target_node text,
    p_target_dsn text,
    p_target_lsn pg_lsn DEFAULT NULL,
    p_connect_timeout_seconds int DEFAULT 10,
    p_statement_timeout_ms int DEFAULT 600000,
    p_lock_timeout_ms int DEFAULT 30000,
    p_fence_timeout_ms int DEFAULT 300000,
    p_poll_interval_ms int DEFAULT 100
)
RETURNS pgl_validate.fence_attempt
LANGUAGE plpgsql
VOLATILE
AS $$
DECLARE
    target_lsn pg_lsn := COALESCE(p_target_lsn, pg_current_wal_lsn());
    observation record;
    deadline timestamptz;
    attempt_status text;
    result_row pgl_validate.fence_attempt;
BEGIN
    IF p_run_id IS NULL THEN
        RAISE EXCEPTION 'run_id is required';
    END IF;
    IF p_epoch_seq IS NULL THEN
        RAISE EXCEPTION 'epoch_seq is required';
    END IF;
    IF p_edge_id IS NULL THEN
        RAISE EXCEPTION 'edge_id is required';
    END IF;
    IF p_provider_node IS NULL OR p_target_node IS NULL THEN
        RAISE EXCEPTION 'provider_node and target_node are required';
    END IF;
    IF p_target_dsn IS NULL THEN
        RAISE EXCEPTION 'target_dsn is required';
    END IF;
    IF pg_is_in_recovery() THEN
        RAISE EXCEPTION 'standby fences must be coordinated from a primary'
            USING ERRCODE = '0A000';
    END IF;
    IF p_fence_timeout_ms <= 0 THEN
        RAISE EXCEPTION 'fence_timeout_ms must be greater than zero';
    END IF;
    IF p_poll_interval_ms <= 0 THEN
        RAISE EXCEPTION 'poll_interval_ms must be greater than zero';
    END IF;

    INSERT INTO pgl_validate.run_edge(
        run_id, edge_id, provider_node, target_node, backend,
        subscription, slot_name, origin_name, repsets
    )
    VALUES (
        p_run_id, p_edge_id, p_provider_node, p_target_node, 'standby',
        NULL, NULL, NULL, NULL
    )
    ON CONFLICT (run_id, edge_id) DO UPDATE
    SET provider_node = EXCLUDED.provider_node,
        target_node = EXCLUDED.target_node,
        backend = 'standby',
        subscription = NULL,
        slot_name = NULL,
        origin_name = NULL,
        repsets = NULL;

    INSERT INTO pgl_validate.fence_epoch(run_id, epoch_seq)
    VALUES (p_run_id, p_epoch_seq)
    ON CONFLICT (run_id, epoch_seq) DO NOTHING;

    INSERT INTO pgl_validate.fence_edge(
        run_id, epoch_seq, edge_id, fence_kind, barrier_token, barrier_end_lsn
    )
    VALUES (
        p_run_id,
        p_epoch_seq,
        p_edge_id,
        'standby_replay',
        NULL,
        target_lsn
    )
    ON CONFLICT (run_id, epoch_seq, edge_id) DO UPDATE
    SET fence_kind = 'standby_replay',
        barrier_token = NULL,
        barrier_end_lsn = EXCLUDED.barrier_end_lsn;

    deadline := clock_timestamp() + make_interval(secs => p_fence_timeout_ms / 1000.0);
    LOOP
        SELECT *
        INTO observation
        FROM pgl_validate.remote_standby_replay_status(
            p_target_dsn,
            p_connect_timeout_seconds,
            p_statement_timeout_ms,
            p_lock_timeout_ms
        );

        IF NOT observation.in_recovery THEN
            RAISE EXCEPTION 'standby peer % is not in recovery', p_target_node
                USING ERRCODE = '0A000';
        END IF;

        EXIT WHEN observation.replay_lsn >= target_lsn;

        IF clock_timestamp() >= deadline THEN
            attempt_status := 'timeout';
            EXIT;
        END IF;

        PERFORM pg_sleep(p_poll_interval_ms / 1000.0);
    END LOOP;

    SELECT *
    INTO result_row
    FROM pgl_validate.record_fence_attempt(
        p_run_id,
        p_epoch_seq,
        p_edge_id,
        target_lsn,
        observation.replay_lsn,
        true,
        NULL,
        attempt_status
    );

    RETURN result_row;
END
$$;

-- Build the planner-visible key-range predicate shared by chunk checksums and
-- row localization. Boundary bytes are UTF-8 JSON objects whose keys are the
-- comparison-key column names; SQL generation casts each value to the actual
-- column type so the final predicate is over table columns and typed constants.
CREATE FUNCTION pgl_validate.plan_key_range_predicate(
    rel regclass,
    key_cols text[],
    lo bytea,
    hi bytea
)
RETURNS text
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
    lower_doc jsonb;
    upper_doc jsonb;
    key_exprs text[] := ARRAY[]::text[];
    lower_values text[] := ARRAY[]::text[];
    upper_values text[] := ARRAY[]::text[];
    predicates text[] := ARRAY[]::text[];
    attr_rec record;
    seen_keys int := 0;
    value_text text;
BEGIN
    IF lo IS NULL AND hi IS NULL THEN
        RETURN 'true';
    END IF;

    IF key_cols IS NULL OR cardinality(key_cols) = 0 THEN
        RAISE EXCEPTION 'bounded chunks require a comparison key';
    END IF;

    IF lo IS NOT NULL THEN
        lower_doc := convert_from(lo, 'UTF8')::jsonb;
        IF jsonb_typeof(lower_doc) <> 'object' THEN
            RAISE EXCEPTION 'lower chunk boundary must be a JSON object';
        END IF;
    END IF;

    IF hi IS NOT NULL THEN
        upper_doc := convert_from(hi, 'UTF8')::jsonb;
        IF jsonb_typeof(upper_doc) <> 'object' THEN
            RAISE EXCEPTION 'upper chunk boundary must be a JSON object';
        END IF;
    END IF;

    FOR attr_rec IN
        SELECT ord.ordinality,
               a.attname,
               format_type(a.atttypid, a.atttypmod) AS type_sql
        FROM unnest(key_cols) WITH ORDINALITY AS ord(col_name, ordinality)
        JOIN pg_attribute a
          ON a.attrelid = rel
         AND a.attname = ord.col_name
         AND a.attnum > 0
         AND NOT a.attisdropped
        ORDER BY ord.ordinality
    LOOP
        seen_keys := seen_keys + 1;
        key_exprs := key_exprs || format('t.%I', attr_rec.attname);

        IF lower_doc IS NOT NULL THEN
            value_text := lower_doc ->> attr_rec.attname;
            IF value_text IS NULL THEN
                RAISE EXCEPTION 'lower chunk boundary is missing non-null key column %', attr_rec.attname;
            END IF;
            lower_values := lower_values || format('%L::%s', value_text, attr_rec.type_sql);
        END IF;

        IF upper_doc IS NOT NULL THEN
            value_text := upper_doc ->> attr_rec.attname;
            IF value_text IS NULL THEN
                RAISE EXCEPTION 'upper chunk boundary is missing non-null key column %', attr_rec.attname;
            END IF;
            upper_values := upper_values || format('%L::%s', value_text, attr_rec.type_sql);
        END IF;
    END LOOP;

    IF seen_keys <> cardinality(key_cols) THEN
        RAISE EXCEPTION 'one or more key columns are not present on %', rel::text;
    END IF;

    IF lower_doc IS NOT NULL THEN
        predicates := predicates || CASE
            WHEN seen_keys = 1 THEN format('%s >= %s', key_exprs[1], lower_values[1])
            ELSE format('(%s) >= (%s)', array_to_string(key_exprs, ', '), array_to_string(lower_values, ', '))
        END;
    END IF;

    IF upper_doc IS NOT NULL THEN
        predicates := predicates || CASE
            WHEN seen_keys = 1 THEN format('%s < %s', key_exprs[1], upper_values[1])
            ELSE format('(%s) < (%s)', array_to_string(key_exprs, ', '), array_to_string(upper_values, ', '))
        END;
    END IF;

    RETURN array_to_string(predicates, ' AND ');
END
$$;

-- Plan exact key ranges from a local participant scan. The returned boundaries
-- use the same bytea JSON-object representation consumed by
-- plan_key_range_predicate(), so each range can be fed directly into
-- plan_chunk_sql() or plan_localize_sql().
CREATE FUNCTION pgl_validate.plan_key_ranges(
    rel regclass,
    key_cols text[],
    p_lo bytea DEFAULT NULL,
    p_hi bytea DEFAULT NULL,
    chunk_target_rows integer DEFAULT 50000,
    row_filter_sql text DEFAULT NULL
)
RETURNS TABLE (
    chunk_id bigint,
    lo bytea,
    hi bytea,
    n_rows bigint
)
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
    rel_sql text;
    selected_key_cols text[];
    key_json_args text;
    order_args text;
    filter_sql text;
    range_sql text;
    where_sql text;
BEGIN
    IF chunk_target_rows IS NULL OR chunk_target_rows <= 0 THEN
        RAISE EXCEPTION 'chunk_target_rows must be greater than zero';
    END IF;

    IF key_cols IS NULL OR cardinality(key_cols) = 0 THEN
        RAISE EXCEPTION 'range planning requires a comparison key';
    END IF;

    SELECT format('%I.%I', n.nspname, c.relname)
    INTO rel_sql
    FROM pg_class c
    JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE c.oid = rel;

    IF rel_sql IS NULL THEN
        RAISE EXCEPTION 'relation % does not exist', rel;
    END IF;

    SELECT array_agg(a.attname ORDER BY ord.ordinality)
    INTO selected_key_cols
    FROM unnest(key_cols) WITH ORDINALITY AS ord(col_name, ordinality)
    JOIN pg_attribute a
      ON a.attrelid = rel
     AND a.attname = ord.col_name
     AND a.attnum > 0
     AND NOT a.attisdropped;

    IF selected_key_cols IS NULL OR cardinality(selected_key_cols) <> cardinality(key_cols) THEN
        RAISE EXCEPTION 'one or more key columns are not present on %', rel::text;
    END IF;

    SELECT
        string_agg(format('%L, t.%I', a.attname, a.attname), ', ' ORDER BY ord.ordinality),
        string_agg(format('t.%I', a.attname), ', ' ORDER BY ord.ordinality)
    INTO key_json_args, order_args
    FROM unnest(selected_key_cols) WITH ORDINALITY AS ord(col_name, ordinality)
    JOIN pg_attribute a
      ON a.attrelid = rel
     AND a.attname = ord.col_name
     AND a.attnum > 0
     AND NOT a.attisdropped;

    filter_sql := CASE
        WHEN row_filter_sql IS NULL OR btrim(row_filter_sql) = '' THEN 'true'
        ELSE format('(%s)', row_filter_sql)
    END;
    range_sql := pgl_validate.plan_key_range_predicate(rel, key_cols, p_lo, p_hi);
    where_sql := COALESCE(
        NULLIF(
            concat_ws(
                ' AND ',
                NULLIF(filter_sql, 'true'),
                NULLIF(range_sql, 'true')
            ),
            ''
        ),
        'true'
    );

    RETURN QUERY EXECUTE format(
        $range_sql$
        WITH ordered AS MATERIALIZED (
            SELECT
                row_number() OVER (ORDER BY %1$s) AS rn,
                convert_to(jsonb_build_object(%2$s)::text, 'UTF8') AS boundary
            FROM %3$s t
            WHERE %4$s
        ),
        starts AS (
            SELECT
                (((rn - 1) / %5$s) + 1)::bigint AS planned_chunk_id,
                min(rn) AS start_rn,
                count(*)::bigint AS planned_rows
            FROM ordered
            GROUP BY 1
        ),
        ranges AS (
            SELECT
                s.planned_chunk_id,
                s.start_rn,
                s.planned_rows,
                lead(s.start_rn) OVER (ORDER BY s.planned_chunk_id) AS next_start_rn
            FROM starts s
        )
        SELECT
            r.planned_chunk_id,
            CASE
                WHEN r.planned_chunk_id = 1 THEN $1::bytea
                ELSE (
                    SELECT o.boundary
                    FROM ordered o
                    WHERE o.rn = r.start_rn
                )
            END AS lo,
            COALESCE(
                (
                    SELECT o.boundary
                    FROM ordered o
                    WHERE o.rn = r.next_start_rn
                ),
                $2::bytea
            ) AS hi,
            r.planned_rows
        FROM ranges r
        ORDER BY r.planned_chunk_id
        $range_sql$,
        order_args,
        key_json_args,
        rel_sql,
        where_sql,
        chunk_target_rows
    )
    USING p_lo, p_hi;
END
$$;

-- Generate the planner-visible SQL used to checksum a table chunk. Columns are
-- sorted by name before they are passed as heterogeneous VARIADIC row_digest
-- arguments; callers may EXPLAIN the returned SQL directly on a participant.
CREATE FUNCTION pgl_validate.plan_chunk_sql(
    rel regclass,
    key_cols text[],
    lo bytea,
    hi bytea,
    cols text[],
    repsets text[] DEFAULT NULL,
    row_filter_sql text DEFAULT NULL,
    include_set_hash boolean DEFAULT false
)
RETURNS text
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
    rel_sql text;
    enc_modes int[];
    digest_args text;
    row_digest_expr text;
    selected_cols text[];
    filter_sql text;
    range_sql text;
    where_sql text;
BEGIN
    SELECT format('%I.%I', n.nspname, c.relname)
    INTO rel_sql
    FROM pg_class c
    JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE c.oid = rel;

    IF rel_sql IS NULL THEN
        RAISE EXCEPTION 'relation % does not exist', rel;
    END IF;

    IF cols IS NULL OR cardinality(cols) = 0 THEN
        SELECT array_agg(a.attname ORDER BY a.attname)
        INTO selected_cols
        FROM pg_attribute a
        WHERE a.attrelid = rel
          AND a.attnum > 0
          AND NOT a.attisdropped;
    ELSE
        SELECT array_agg(a.attname ORDER BY a.attname)
        INTO selected_cols
        FROM unnest(cols) requested(col_name)
        JOIN pg_attribute a
          ON a.attrelid = rel
         AND a.attname = requested.col_name
         AND a.attnum > 0
         AND NOT a.attisdropped;

        IF cardinality(selected_cols) IS DISTINCT FROM cardinality(cols) THEN
            RAISE EXCEPTION 'one or more requested columns are not present on %', rel::text;
        END IF;
    END IF;

    IF selected_cols IS NULL OR cardinality(selected_cols) = 0 THEN
        RAISE EXCEPTION 'no comparable columns found on %', rel::text;
    END IF;

    SELECT
        array_agg(pgl_validate.column_encoding_mode(a.atttypid) ORDER BY a.attname),
        string_agg(format('t.%I', a.attname), ', ' ORDER BY a.attname)
    INTO enc_modes, digest_args
    FROM pg_attribute a
    WHERE a.attrelid = rel
      AND a.attname = ANY (selected_cols)
      AND a.attnum > 0
      AND NOT a.attisdropped;

    row_digest_expr := format(
        'pgl_validate.row_digest(%L::int[], %s)',
        enc_modes::text,
        digest_args
    );
    filter_sql := CASE
        WHEN row_filter_sql IS NULL OR btrim(row_filter_sql) = '' THEN 'true'
        ELSE format('(%s)', row_filter_sql)
    END;
    range_sql := pgl_validate.plan_key_range_predicate(rel, key_cols, lo, hi);
    where_sql := COALESCE(
        NULLIF(
            concat_ws(
                ' AND ',
                NULLIF(filter_sql, 'true'),
                NULLIF(range_sql, 'true')
            ),
            ''
        ),
        'true'
    );

    IF include_set_hash THEN
        RETURN format(
            'WITH digests AS MATERIALIZED (SELECT %s AS rd FROM %s t WHERE %s), aggregate AS (SELECT count(*)::bigint AS n_rows, pgl_validate.lthash_bytes(pgl_validate.lthash(rd)) AS lthash FROM digests) SELECT aggregate.n_rows, aggregate.lthash, (SELECT pgl_validate.hash_digest_array(COALESCE(array_agg(rd ORDER BY rd), ARRAY[]::bytea[])) FROM digests) AS set_hash FROM aggregate',
            row_digest_expr,
            rel_sql,
            where_sql
        );
    END IF;

    RETURN format(
        'SELECT count(*)::bigint AS n_rows, pgl_validate.lthash_bytes(pgl_validate.lthash(%s)) AS lthash, NULL::bytea AS set_hash FROM %s t WHERE %s',
        row_digest_expr,
        rel_sql,
        where_sql
    );
END
$$;

